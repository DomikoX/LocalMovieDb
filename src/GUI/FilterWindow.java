    private int parseYear(String year) {
        try {
            if (year.length() == 4) {
                return Integer.parseInt(year);
            }
            if (year.length() == 5) {
                return Integer.parseInt(year.substring(0, 4));
            }
            if (year.length() == 9) {
                return Integer.parseInt(year.substring(5, 9));
            }
        } catch (Exception e) {
            return 0;
        }
        return 0;
    }
