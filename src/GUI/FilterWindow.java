    private void jButtonFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFilterActionPerformed
        ArrayList<MovieRecord> helper = new ArrayList<>();

        boolean byTitle = !jTextFieldTitle.getText().isEmpty();
        boolean byGenre = !((String) jComboBox1.getSelectedItem()).equals("---");
        boolean byYear = !jTextFieldYear.getText().isEmpty();

        String title = jTextFieldTitle.getText();
        String genre = (String) jComboBox1.getSelectedItem();
        int year = byYear ? Integer.parseInt(jTextFieldYear.getText()) : 0;

        for (MovieRecord m : movLib.getMovies()) {
            if (!m.haveAllData()) {
                if (byTitle && m.getFilename().contains(jTextFieldTitle.getText())) {
                    helper.add(m);
                }
                continue;
            }
            int movieYear = parseYear(m.getMovie().getYear());
            if (byTitle && !(m.getFilename().contains(jTextFieldTitle.getText()) || m.getMovie().getTitle().equals(jTextFieldTitle.getText()))) {
                continue;
            }
            if (byGenre && !m.getMovie().getGenresInOneString().contains((String) jComboBox1.getSelectedItem())) {
                continue;
            }
            if (byYear && !(year <= movieYear)) {
                continue;
            }

            helper.add(m);

        }

        table.setModel(new MovieRecordTableModel(helper.isEmpty() ? movLib.getMovies() : helper));
        setVisible(false); //you can't see me!
        dispose(); //Destroy the JFrame object

    }//GEN-LAST:event_jButtonFilterActionPerformed


